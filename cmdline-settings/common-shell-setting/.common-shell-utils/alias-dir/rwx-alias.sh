# =================== user =====================
alias ur="chmod u+r"
alias uR="chmod u-r"

alias uw="chmod u+w"
alias uW="chmod u-w"

alias ux="chmod u+x"
alias uX="chmod u-x"


# =================== all  =====================
alias ar="chmod a+r"        # ar 和命令ar冲突
alias aR="chmod a-r"

alias aw="chmod a+w"
alias aW="chmod a-w"

alias ax="chmod a+x"
alias aX="chmod a-x"

# =================== other =====================
alias ow="chmod o+w"
alias oW="chmod o-w"        # 主要解决Windows copy到linux的目录, 所有用户都有write权限.
